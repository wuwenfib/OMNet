#include "Router.h"
Define_Module(Router);

void Router::initialize()
{
    // --------------------------------------------------------
    // 【新概念】gateSize("out") — 查询门阵列的大小
    //
    // 返回 out[] 数组里有多少个门。
    // 这里会打印 2，因为 Router.ned 里写的是 output out[2]。
    // --------------------------------------------------------
    EV << "【Router】初始化，out 门阵列大小 = " << gateSize("out") << endl;
}

void Router::handleMessage(cMessage *msg)
{
    // 转型：cMessage* → DataPacket*
    DataPacket *pkt = check_and_cast<DataPacket *>(msg);

    int idx = pkt->getDestIndex();   // 从包里读目标门索引

    EV << "【t=" << simTime() << "s】Router 收到包（srcAddr="
       << pkt->getSrcAddr() << "，destIndex=" << idx << "）→ ";

    // --------------------------------------------------------
    // 【新概念】gateSize() 做边界检查，防止越界崩溃
    //
    // 如果索引合法（0 ≤ idx < 门数量），就转发；
    // 否则丢包并打印日志。
    // --------------------------------------------------------
    if (idx >= 0 && idx < gateSize("out")) {

        EV << "从 out[" << idx << "] 转发" << endl;

        // --------------------------------------------------------
        // 【新概念】send(pkt, "门名", 索引) — 从门阵列的指定门发出
        //
        // 三参数版本的 send：
        //   第 1 个参数：要发送的消息
        //   第 2 个参数：门的名字（字符串）
        //   第 3 个参数：门数组的下标（int）
        //
        // 对比练习 1/2 里的两参数版：send(pkt, "gate$o")
        // 那是单个门；这里是门阵列，所以需要第三个参数指定哪一个。
        // --------------------------------------------------------
        send(pkt, "out", idx);

    } else {
        EV << "索引越界，丢包" << endl;
        delete pkt;
    }
}
